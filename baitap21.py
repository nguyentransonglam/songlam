so_nguyen = int(input(" Nhập số nguyên bất kì từ 0 đến 9 là: "))
if so_nguyen  == 0:
    print("Không")
elif so_nguyen  == 1:
    print("Một")
elif so_nguyen  == 2:
    print("Hai")
elif so_nguyen  == 3:
    print("Ba")
elif so_nguyen  == 4:
    print("Bốn")
elif so_nguyen  == 5:
    print("Năm")
elif so_nguyen  == 6:
    print("Sáu")
elif so_nguyen  == 7:
    print("Bảy")
elif so_nguyen  == 8:
    print("Tám")
elif so_nguyen == 9:
    print("Chín")
else:
    print("Không đọc được.")
    
