a = int(input("Nhập số nguyên thứ nhất là: "))
b = int(input("Nhập số nguyên thứ hai là : "))
c = int(input("Nhập số nguyên thứ ba là: "))
so_lon_nhat = max(a,b,c)
so_nho_nhat = min(a,b,c)
print("Số lớn nhất là: ",so_lon_nhat)
print("Số nhỏ nhất là: ",so_nho_nhat)